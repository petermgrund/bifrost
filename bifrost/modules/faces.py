"""Faces module — person links and face-rectangle MediaRefs.

Ported from immich-to-gramps (person_linker_gui.py + run_link_faces /
run_repad_faces in immich_to_gramps.py), with bifrost's SQLite as the source
of truth for links and a write-through person_map.yaml export for the legacy
pipeline (removed in Phase 2).

Long operations (link_faces, repad_faces) are async generators yielding
SyncEvents; preview is the same generator with apply=False.
"""

from __future__ import annotations

import logging
import sqlite3
from datetime import datetime
from pathlib import Path
from typing import AsyncIterator

import yaml

from ..core.clients import GrampsClient, ImmichClient
from ..core.clients.gramps import person_display_name
from ..core.events import SyncEvent

log = logging.getLogger("bifrost.faces")

# Default expansion applied to Immich face boxes when writing them to Gramps.
# 0.15 grows each side by 15% of the box dimension. Assets tagged
# Sync/ManualFaces opt out (tight crop, and re-pad leaves them alone).
FACE_PAD_FACTOR = 0.15
MANUAL_FACES_TAG = "Sync/ManualFaces"
IMMICH_ID_ATTR = "Immich ID"

PERSON_MAP_HEADER = """# Person mapping between Gramps and Immich
#
# GENERATED BY BIFROST — do not hand-edit; edit links in the Bifrost faces
# page instead. This export exists so the legacy immich_to_gramps.py
# --link-faces keeps working until bifrost's sync module replaces it.

"""


# ---------------------------------------------------------------------------
# Pure functions
# ---------------------------------------------------------------------------

def immich_face_to_gramps_rect(face: dict, pad_factor: float = 0.0) -> list[int] | None:
    """Convert an Immich face bounding box to Gramps rect percentages.

    Immich provides boundingBoxX1/Y1/X2/Y2 in pixels plus imageWidth/Height.
    Gramps expects [left, top, right, bottom] as integer percentages (0-100).
    pad_factor expands the box outward by that fraction of its width/height on
    each side before converting; the result clamps to [0, 100].
    """
    iw = face.get("imageWidth")
    ih = face.get("imageHeight")
    if not iw or not ih:
        return None

    x1 = face.get("boundingBoxX1", 0)
    y1 = face.get("boundingBoxY1", 0)
    x2 = face.get("boundingBoxX2", 0)
    y2 = face.get("boundingBoxY2", 0)

    if pad_factor > 0:
        bw = x2 - x1
        bh = y2 - y1
        x1 -= bw * pad_factor
        x2 += bw * pad_factor
        y1 -= bh * pad_factor
        y2 += bh * pad_factor

    return [
        max(0, min(100, round(x1 * 100 / iw))),
        max(0, min(100, round(y1 * 100 / ih))),
        max(0, min(100, round(x2 * 100 / iw))),
        max(0, min(100, round(y2 * 100 / ih))),
    ]


# ---------------------------------------------------------------------------
# Person links (SQLite is the source of truth; YAML export is a compat shim)
# ---------------------------------------------------------------------------

def list_links(conn: sqlite3.Connection) -> list[dict]:
    rows = conn.execute(
        "SELECT gramps_handle, immich_person_id, label FROM person_links ORDER BY rowid"
    ).fetchall()
    out = []
    for r in rows:
        entry = {"gramps_handle": r["gramps_handle"], "immich_person_id": r["immich_person_id"]}
        if r["label"]:
            entry["label"] = r["label"]
        out.append(entry)
    return out


def person_map_dict(conn: sqlite3.Connection) -> dict[str, str]:
    """{immich_person_id: gramps_handle} — the shape the apply logic wants."""
    return {
        e["immich_person_id"]: e["gramps_handle"] for e in list_links(conn)
    }


def set_link(
    conn: sqlite3.Connection,
    gramps_handle: str,
    immich_person_id: str,
    label: str | None,
    export_path: Path | None,
) -> list[dict]:
    """Create or replace the link for a Gramps person (legacy semantics:
    one Immich person per Gramps handle)."""
    now = datetime.now().isoformat(timespec="seconds")
    with conn:
        conn.execute("DELETE FROM person_links WHERE gramps_handle = ?", (gramps_handle,))
        conn.execute(
            "INSERT INTO person_links (gramps_handle, immich_person_id, label, created_at)"
            " VALUES (?, ?, ?, ?)",
            (gramps_handle, immich_person_id, (label or "").strip() or None, now),
        )
    export_person_map(conn, export_path)
    return list_links(conn)


def delete_link(
    conn: sqlite3.Connection, gramps_handle: str, export_path: Path | None
) -> bool:
    with conn:
        cur = conn.execute(
            "DELETE FROM person_links WHERE gramps_handle = ?", (gramps_handle,)
        )
    if cur.rowcount == 0:
        return False
    export_person_map(conn, export_path)
    return True


def export_person_map(conn: sqlite3.Connection, path: Path | None) -> None:
    """Write-through shim: regenerate the legacy person_map.yaml."""
    if path is None:
        return
    if not path.parent.is_dir():
        log.warning("person_map export dir missing (%s) — skipping export", path.parent)
        return
    with open(path, "w") as fh:
        fh.write(PERSON_MAP_HEADER)
        yaml.dump(
            {"people": list_links(conn)},
            fh, sort_keys=False, default_flow_style=False, allow_unicode=True,
        )
    log.info("Exported person map to %s", path)


