async def synced_immich_media(gramps: GrampsClient) -> dict[str, dict]:
    """{immich_asset_id: gramps_media_obj} for media with an Immich ID attribute."""
    synced: dict[str, dict] = {}
    for m in await gramps.list_media():
        for attr in m.get("attribute_list", []):
            if attr.get("type") == IMMICH_ID_ATTR:
                synced[attr["value"]] = m
                break
    return synced


async def manual_face_asset_ids(immich: ImmichClient) -> set[str]:
    try:
        tag_id = await immich.resolve_tag_id(MANUAL_FACES_TAG)
        if not tag_id:
            return set()
        return await immich.search_asset_ids_by_tag(tag_id)
    except Exception as exc:  # noqa: BLE001 — tag lookup is best-effort
        log.warning("Could not look up %s tag: %s", MANUAL_FACES_TAG, exc)
        return set()


